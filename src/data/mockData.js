// Helper Functions
export const getMovieById = (id) => {
  return movies.find(movie => movie.id === parseInt(id));
};

export const getMoviesByCategory = (category) => {
  return movies.filter(movie => movie.category === category);
};

export const getAllGenres = () => {
  const genresSet = new Set();
  movies.forEach(movie => {
    movie.genres.forEach(genre => genresSet.add(genre));
  });
  return Array.from(genresSet).sort();
};

export const searchMovies = (query) => {
  const lowerQuery = query.toLowerCase();
  return movies.filter(movie =>
    movie.title.toLowerCase().includes(lowerQuery) ||
    movie.description.toLowerCase().includes(lowerQuery) ||
    movie.genres.some(genre => genre.toLowerCase().includes(lowerQuery)) ||
    movie.cast.some(actor => actor.toLowerCase().includes(lowerQuery)) ||
    movie.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};

export const getMoviesByGenre = (genre) => {
  return movies.filter(movie => 
    movie.genres.some(g => g.toLowerCase() === genre.toLowerCase())
  );
};